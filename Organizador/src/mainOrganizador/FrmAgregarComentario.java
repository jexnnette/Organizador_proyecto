    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        UUID uuid = UUID.randomUUID();
        String idComentario = uuid.toString();
        String nombre = jTextField1.getText().trim().replaceAll("\\s*\\|\\s*", " pipe operator ");
        String idTarea = EstadoGlobal.TransferenciaTarea.getId();
        try {    
            if (!nombre.isEmpty() && !nombre.isBlank() && nombre.length() >= 0) {
                ArrayList<Comentarios> lista = new ArrayList();
                Comentarios comentario = new Comentarios(); 
                comentario.setIdComentario(idComentario);
                comentario.setIdTarea(idTarea);           
                comentario.setComentario(nombre);
                lista.add(comentario);
                EstadoGlobal.TransferenciaTarea.aniadirComentariosTarea(lista);
                JOptionPane.showMessageDialog(null, "Comentario Agregada Correctamente!");
                jTextField1.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "No Puede Dejar El Nombre Vacio!");
                 jTextField1.setText("");
            }
        }catch (NumberFormatException | HeadlessException ex){
            JOptionPane.showMessageDialog(null, "Error! " + ex);
             jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

